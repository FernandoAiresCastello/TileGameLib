        public void Swap(int first, int second)
        {
            TilePixels temp = Get(first);
            Set(first, Get(second));
            Set(second, temp);
        }

        public void Truncate(int count)
        {
            Pixels = Pixels.GetRange(0, count);
        }

        public void LoadFromImage(string file)
        {
            using (Bitmap image = (Bitmap)Image.FromFile(file))
            {
                int width = image.Width;
                int height = image.Height;

                int[,] pixelArray = new int[width, height];

                for (int y = 0; y < height; y++)
                {
                    for (int x = 0; x < width; x++)
                    {
                        int argb = image.GetPixel(x, y).ToArgb();
                        if (argb.Equals(ImagePixelOnColor.ToArgb()))
                            pixelArray[x, y] = ImagePixelOnValue;
                        else if (argb.Equals(ImagePixelOffColor.ToArgb()))
							pixelArray[x, y] = ImagePixelOffValue;
                    }
                }

                ParseFromArray(pixelArray, width, height);
            }
        }

        public void ParseFromArray(int[,] array, int width, int height)
        {
            Pixels.Clear();

            int cols = width / 8;
			int rows = width / 8;
            int col = 0;
            int row = 0;

			bool done = false;

            int tileX = 0;
            int tileY = 0;
            int pixelX = 0;
            int pixelY = 0;
            string binary = string.Empty;

            while (!done)
            {
                binary += array[tileX + pixelX, tileY + pixelY].ToString();

                if (binary.Length == 64)
                {
                    Add(binary);
                    binary = string.Empty;

                    pixelX = 0;
                    pixelY = 0;

                    tileX += 8;
                    col++;

                    if (col >= cols)
                    {
						tileX = 0;
						col = 0;
						tileY += 8;
                        row++;

                        if (row >= rows)
                        {
                            done = true;
                            break;
                        }
					}
                }
                else
                {
                    pixelX++;

                    if (pixelX >= 8)
                    {
                        pixelX = 0;
                        pixelY++;
                    }
                }
            }
        }


#include "TGL_font.h"

font::font()
{
	for (int i = 0; i < 256; i++) {
		add_blank();
	}
	init_default();
}
font::~font()
{
	for (int i = 0; i < chars.size(); i++) {
		delete chars[i];
		chars[i] = nullptr;
	}
	chars.clear();
}
void font::set(byte ch, string pixels)
{
	chars[ch]->get(0).pixels = pixels;
}
void font::add_blank()
{
	tile* blank_tile = new tile();
	blank_tile->add(
		"00000000"
		"00000000"
		"00000000"
		"00000000"
		"00000000"
		"00000000"
		"00000000"
		"00000000", 0x000000, 0xffffff, 0x808080, 0x808080
	);
	chars.push_back(blank_tile);
}
void font::set_blank(byte ch)
{
	chars[ch]->get(0).pixels = String::Repeat('0', tile::size);
}
